#include <unistd.h>

int	main(int argc, char **argv)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	(void)argc;
	while (argv[i][j] != '\0')
	{
		write(1, &argv[i][j], 1);
		j++;
	}
}
